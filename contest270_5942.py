import itertools

def solution(digits):
    permutations = itertools.permutations(digits, 3)
    permutations_set = set(permutations)
    # print(permutations_set)

    result = []
    for permutation in permutations_set:
        # print(permutation)
        if permutation[0] == 0 or permutation[2] not in [0, 2, 4, 6, 8]:
            continue
        integer = int(''.join(str(digit) for digit in permutation))
        result.append(integer)
        # print(integer)
    
    return sorted(result)
